unsigned char beep_delay_time_count=0x00;


/**********************************************************************
*函数名称：init_beep
*描述：蜂鸣器
*返回类型：void
*输入形参：void
***********************************************************************/
void init_beep(void)
{
	BEEP_TRIS =0;  //蜂鸣器控制脚RA0设置为输出模式
	
	BEEP_OFF;   //关闭
}


/**********************************************************************
*函数名称：BEEP_SPEAK
*描述：蜂鸣器
*返回类型：void
*输入形参：void
***********************************************************************/
void BEEP_SPEAK(void)
{
   unsigned int i=0;

   BEEP_ON;
   
   beep_delay_time_count=50;   //利用中断产生延迟效果 20ms

}